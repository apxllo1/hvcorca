        local args = {...}
        local last = s
        for i, v in ipairs(args) do
            if type(v) == "string" then
                last = last:FindFirstChild(v)
            else
                last = v
            end
        end
        if modules[last] then
            local m = modules[last]
            if not m.loaded then
                m.loaded = true
                local innerFunc = m.fn()
                setfenv(innerFunc, hEnv(last:GetFullName()))
                m.data = innerFunc()
            end
            return m.data
        end
        return require(last)
    end,
    getModule = function(self, s, ...)
        return _G.require(s)
    end
}

