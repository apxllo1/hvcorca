--[[
    Havoc Studios Runtime Engine
    This was generated by /bundle.release.ts and is not meant to be modified. 
]]
local instanceFromId, modules = {}, {}
local _G = (getfenv and getfenv()) or _G

-- Standard TS Runtime setup
local TS = {
    import = function(self, s, ...) 
        local args = {...}
        local last = s
        for i, v in ipairs(args) do
            if type(v) == "string" then last = last:FindFirstChild(v)
            else last = v end
        end
        return require(last)
    end,
    getModule = function(self, s, ...) 
        -- Simplified module getter for bundled env
        return _G.require(s) 
    end
}

local function hEnv(id)
    local inst = instanceFromId[id]
    local env = setmetatable({
        script = inst,
        require = function(target)
            if modules[target] then
                local m = modules[target]
                if not m.loaded then
                    m.loaded = true
                    m.data = m.fn()(...)
                end
                return m.data
            end
            return require(target)
        end
    }, { __index = _G })
    _G[inst] = TS -- CRITICAL: Maps the TS runtime to the fake script object
    return env
end

local function hMod(name, class, id, parentId, fn)
    local inst = Instance.new(class)
    inst.Name = name
    inst.Parent = instanceFromId[parentId]
    instanceFromId[id] = inst
    modules[inst] = { fn = fn, loaded = false }
end

local function hInst(name, class, id, parentId)
    local inst = Instance.new(class)
    inst.Name = name
    inst.Parent = instanceFromId[parentId]
    instanceFromId[id] = inst
end

local function hInit()
    -- Initialize all LocalScripts (Entry Points)
    for inst, m in pairs(modules) do
        if inst:IsA("LocalScript") then
            task.spawn(function()
                setfenv(m.fn(), hEnv(inst:GetFullName()))()
            end)
        end
    end
end

-- Return the API to the bundler
return hInit, hMod, hInst, hEnv
