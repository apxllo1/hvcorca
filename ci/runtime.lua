local instanceFromId, modules = {}, {}
local _G = (getfenv and getfenv()) or _G

local ROOT_PARENT = "ROOT"

local function resolveParent(parentId)
    if parentId == ROOT_PARENT then
        local Players = game:GetService("Players")
        return Players.LocalPlayer:WaitForChild("PlayerGui")
    end
    return instanceFromId[parentId]
end

-- Forward declare hEnv so TS.import and hMod can reference it
local hEnv

local TS = {
    import = function(self, s, ...)
        local args = {...}
        local last = s
        for i, v in ipairs(args) do
            if type(v) == "string" then
                last = last:FindFirstChild(v)
            else
                last = v
            end
        end

        -- Check bundled registry first before falling back to native require
        if modules[last] then
            local m = modules[last]
            if not m.loaded then
                m.loaded = true
                m.data = m.fn()(hEnv(last:GetFullName()))
            end
            return m.data
        end

        return require(last)
    end,

    getModule = function(self, s, ...)
        return _G.require(s)
    end
}

hEnv = function(id)
    local inst = instanceFromId[id]
    local env = setmetatable({
        script = inst,
        require = function(target)
            if modules[target] then
                local m = modules[target]
                if not m.loaded then
                    m.loaded = true
                    m.data = m.fn()(hEnv(target:GetFullName()))
                end
                return m.data
            end
            return require(target)
        end
    }, { __index = _G })
    _G[inst] = TS
    return env
end

local function hMod(name, class, id, parentId, fn)
    local inst = Instance.new(class)
    inst.Name = name
    inst.Parent = resolveParent(parentId)
    instanceFromId[id] = inst
    modules[inst] = { fn = fn, loaded = false }
end

local function hInst(name, class, id, parentId)
    local inst = Instance.new(class)
    inst.Name = name
    inst.Parent = resolveParent(parentId)
    instanceFromId[id] = inst
end

local function hInit()
    for inst, m in pairs(modules) do
        if inst:IsA("LocalScript") then
            task.spawn(function()
                setfenv(m.fn(), hEnv(inst:GetFullName()))()
            end)
        end
    end
end

return hInit, hMod, hInst, hEnv
