local instanceFromId, modules = {}, {}
local _G = (getfenv and getfenv()) or _G
local ROOT_PARENT = "ROOT"

local function resolveParent(parentId)
    if parentId == ROOT_PARENT then
        local Players = game:GetService("Players")
        return Players.LocalPlayer:WaitForChild("PlayerGui")
    end
    local parent = instanceFromId[parentId]
    if not parent then
        error(string.format("[Havoc] resolveParent: no instance registered for id '%s'", tostring(parentId)))
    end
    return parent
end

